def convert_wgs_to_utm(lon, lat):
    global espgCode
    utm_band = str((math.floor((lon + 180) / 6 ) % 60) + 1)
    if len(utm_band) == 1:
        utm_band = '0'+utm_band
    if lat >= 0:
        espgCode = '326' + utm_band
    else:
        espgCode = '327' + utm_band
    return espgCode

